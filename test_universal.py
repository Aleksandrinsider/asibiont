"""Тест универсальности агента - разные типы запросов"""

test_cases = [
    # Команды (должны вызывать инструменты)
    "напомни купить молоко",
    "готово",
    "удали задачу про отчет",
    "перенеси встречу на завтра",
    "покажи задачи",
    
    # Вопросы (должны давать развернутые ответы)
    "как продвигать telegram бот?",
    "что ты умеешь?",
    "какие есть методы монетизации?",
    
    # Контекстные (должны понимать из контекста)
    "сделал",
    "я из москвы работаю программистом",
    
    # Креативные (должны генерировать уникально)
    "придумай идеи для стартапа",
    "дай совет по карьере в IT",
]

print("=" * 80)
print("ТЕСТ УНИВЕРСАЛЬНОСТИ АГЕНТА")
print("=" * 80)

for i, test in enumerate(test_cases, 1):
    print(f"\n{i}. Запрос: '{test}'")
    print("   Ожидание: ", end="")
    
    if any(w in test.lower() for w in ['напомни', 'готово', 'удали', 'перенеси', 'покажи', 'сделал', 'москвы']):
        print("TOOL CALL (вызов инструмента)")
    elif '?' in test or any(w in test.lower() for w in ['как', 'что', 'какие', 'дай', 'придумай']):
        print("РАЗВЕРНУТЫЙ ОТВЕТ (без шаблонов)")
    else:
        print("ЕСТЕСТВЕННЫЙ ОТВЕТ")

print("\n" + "=" * 80)
print("Для полного теста запусти бота и проверь каждый кейс")
print("Критерии успеха:")
print("✓ Команды вызывают инструменты (не говорят 'я создал', а СОЗДАЮТ)")
print("✓ Вопросы получают развернутые ответы (не 'хорошо, понял')")
print("✓ Каждый ответ уникален (нет повторяющихся фраз)")
print("✓ Нет банальностей ('разбить на шаги', 'поставить время')")
print("=" * 80)
