import asyncio
import os
os.environ['FREE_ACCESS_MODE'] = 'true'
from ai_integration import chat_with_ai, set_redis_client

async def test_all_functions():
    """Тестирование всех основных функций AI"""

    # Тестовый user_id
    test_user_id = 123456789

    print("🚀 НАЧИНАЕМ ТЕСТИРОВАНИЕ ВСЕХ ФУНКЦИЙ")
    print("=" * 60)

    test_cases = [
        # Управление задачами
        ("Создай задачу: купить продукты в магазине завтра в 10 утра", "Создание задачи"),
        ("Покажи все мои задачи", "Просмотр задач"),
        ("Заверши задачу 'купить продукты'", "Завершение задачи"),
        ("Добавь напоминание к задаче 'купить продукты' на завтра 9:00", "Добавление напоминания"),
        ("Изменить задачу 'купить продукты' - добавить 'и молоко'", "Редактирование задачи"),
        ("Удалить задачу 'купить продукты'", "Удаление задачи"),

        # Управление подписками
        ("Проверь статус моей подписки", "Проверка статуса подписки"),
        ("Создай платеж для подписки", "Создание платежа подписки"),
        ("Отмени мою подписку", "Отмена подписки"),

        # Управление профилем
        ("Обнови мой профиль: я разработчик из Москвы, люблю Python", "Обновление профиля"),
        ("Найди мне партнеров по интересам", "Поиск партнеров"),

        # Делегирование
        ("Создай задачу для делегирования: разработать логотип", "Создание задачи для делегирования"),
        ("Поручи эту задачу пользователю @testuser", "Делегирование задачи"),

        # Аналитика и советы
        ("Проанализируй мои задачи и дай советы", "Анализ задач"),
        ("У меня проблемы с мотивацией, что посоветуешь?", "Получение советов"),
    ]

    context = []  # Контекст диалога

    for i, (message, description) in enumerate(test_cases, 1):
        print(f"\n{i}. {description}")
        print(f"   Сообщение: {message}")

        try:
            response = await chat_with_ai(
                message=message,
                context=context.copy(),
                user_id=test_user_id
            )

            print(f"   Ответ: {response[:200]}{'...' if len(response) > 200 else ''}")

            # Добавляем в контекст для continuity
            context.append({"role": "user", "content": message})
            context.append({"role": "assistant", "content": response})

            # Ограничиваем контекст
            if len(context) > 10:
                context = context[-10:]

            print("   ✅ УСПЕШНО")

        except Exception as e:
            print(f"   ❌ ОШИБКА: {e}")
            continue

    print("\n" + "=" * 60)
    print("🎉 ТЕСТИРОВАНИЕ ЗАВЕРШЕНО!")
    print("Проверьте логи выше на наличие ошибок.")

if __name__ == "__main__":
    asyncio.run(test_all_functions())