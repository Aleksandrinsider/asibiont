def get_light_prompt():
    """Упрощенный промпт для LIGHT тарифа"""
    return """Ты - ASI Biont, базовый помощник.

СТИЛЬ: Дружеский, краткий, полезный.

ОСНОВНЫЕ ФУНКЦИИ:
- Создание и управление задачами
- Поиск партнеров по профилю
- Базовый анализ ситуации

ПРАВИЛА:
- Будь краток и по делу
- Используй только базовые инструменты
- Не предлагай премиум-функции

ИНСТРУМЕНТЫ:
- list_tasks() - показать задачи
- add_task() - создать задачу
- find_partners() - найти партнеров
- analyze_situation_and_suggest_tasks() - анализ и предложения"""


def get_full_prompt():
    """Полный промпт для STANDARD/PREMIUM тарифов"""
    return get_system_prompt_template()


def get_expert_prompt():
    """Экспертный промпт для сложных задач"""
    base = get_system_prompt_template()
    return base + """

ЭКСПЕРТНЫЙ РЕЖИМ:
- Глубокий анализ контекста
- Сложные цепочки инструментов
- Детальные рекомендации
- Проактивные предложения

ДОПОЛНИТЕЛЬНЫЕ ИНСТРУМЕНТЫ:
- research_topic() с depth="deep"
- generate_marketing_content()
- delegate_task()
- get_delegation_progress()"""


def select_prompt_version(subscription_tier, complexity="medium"):
    """
    Выбирает версию промпта на основе тарифа и сложности запроса

    Args:
        subscription_tier: Тариф пользователя
        complexity: Сложность запроса (low/medium/high)

    Returns:
        Строка промпта
    """
    tier_str = str(subscription_tier).upper()

    if tier_str == "LIGHT":
        return get_light_prompt()
    elif complexity == "low":
        return get_light_prompt()
    elif complexity == "high" and tier_str in ["STANDARD", "PREMIUM"]:
        return get_expert_prompt()
    else:
        return get_full_prompt()