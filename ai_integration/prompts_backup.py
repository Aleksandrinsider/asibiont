        if tier_upper in ['STANDARD', 'PREMIUM']:
            tier_info += "\n\nУ пользователя уже есть активная подписка."
        else:
            tier_info += "\n\nЕсли пользователь хочет делегировать задачи, предложи обновить подписку."

