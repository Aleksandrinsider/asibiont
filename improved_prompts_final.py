def improved_classify_intent(message: str, mentions_str: str = "") -> dict:
    """Улучшенная классификация намерений пользователя с поддержкой русского языка"""
    message_lower = message.lower().strip()

    # Проверяем наличие временных указаний для задач
    time_indicators = [
        'через', 'в', 'на', 'завтра', 'послезавтра', 'сегодня', 'вечером', 'утром', 'днем',
        'ночью', 'минут', 'час', 'день', 'недел', 'месяц', 'год', 'январ', 'феврал', 'март',
        'апрел', 'май', 'июн', 'июл', 'август', 'сентябр', 'октябр', 'ноябр', 'декабр',
        'понедельник', 'вторник', 'среда', 'четверг', 'пятница', 'суббота', 'воскресенье'
    ]
    has_time = any(indicator in message_lower for indicator in time_indicators)

    # Извлекаем reminder_time из сообщения
    reminder_time = None
    if has_time:
        # Ищем паттерны времени
        time_patterns = [
            r'(через\s+.+)',
            r'(в\s+\d{1,2}:\d{2})',
            r'(завтра\s+в\s+\d{1,2}:\d{2})',
            r'(сегодня\s+в\s+\d{1,2}:\d{2})',
        ]
        for pattern in time_patterns:
            match = re.search(pattern, message_lower)
            if match:
                reminder_time = match.group(1)
                break

    # Шаблоны для различных команд (проверяем ДО приветствий!)
    patterns = {
        'add_task': [
            r'^напомни(?:ть)?\s+(.+)',
            r'^добавь\s+(.+)',
            r'^запомни\s+(.+)',
            r'^создай\s+задачу\s+(.+)',
            r'^новая\s+задача\s+(.+)',
            r'^задача\s+(.+)',
            r'^напомни\s+о\s+(.+)',
            r'^напомни\s+мне\s+(.+)',
            r'напомни(?:ть)?\s+мне',
            r'добавь\s+задачу',
            r'запомни\s+задачу'
        ],
        'list_tasks': [
            r'покажи\s+задачи',
            r'список\s+задач',
            r'мои\s+задачи',
            r'что\s+на\s+сегодня',
            r'что\s+запланировано',
            r'какие\s+задачи',
            r'^список$',
            r'^задачи$'
        ],
        'complete_task': [
            r'сделал\s+(.+)',
            r'выполнил\s+(.+)',
            r'завершил\s+(.+)',
            r'готово\s+(.+)',
            r'^готово$',
            r'сделано\s+(.+)',
            r'завершить\s+(.+)',
            r'я\s+созвонился',
            r'я\s+выполнил',
            r'я\s+позвонил',
            r'я\s+сделал'
        ],
        'delegate_task': [
            r'@(\w+)\s+(.+)',
            r'поручи\s+@(\w+)\s+(.+)',
            r'делегируй\s+@(\w+)\s+(.+)',
            r'передай\s+@(\w+)\s+(.+)'
        ],
        'delete_all_tasks': [
            r'удали\s+все',
            r'очисти\s+все',
            r'удалить\s+все\s+задачи',
            r'очистить\s+список'
        ],
        'update_profile': [
            r'мой\s+город',
            r'я\s+разработчик',
            r'я\s+работаю',
            r'мой\s+профиль',
            r'обнови\s+профиль',
            r'заполни\s+профиль'
        ]
    }

    # Проверяем каждый паттерн
    for intent_type, pattern_list in patterns.items():
        for pattern in pattern_list:
            match = re.search(pattern, message_lower)
            if match:
                params = {"has_time": has_time}
                if intent_type == "add_task" and match.groups():
                    params["task_title"] = match.group(1).strip()
                elif intent_type == "complete_task" and match.groups():
                    params["task_title"] = match.group(1).strip()
                return {
                    "type": intent_type,
                    "confidence": 0.9,
                    "params": params
                }

    # Простые приветствия - проверяем ПОСЛЕ задач
    greetings = [
        r'^привет$', r'^здравствуй$', r'^добрый день$', r'^доброе утро$', r'^добрый вечер$',
        r'^хай$', r'^hello$', r'^hi$', r'^hey$', r'^ку$', r'^здорово$',
        r'^привет!$', r'^здравствуй!$'
    ]
    if any(re.search(greeting, message_lower) for greeting in greetings):
        return {
            "type": "chat",
            "confidence": 0.95,
            "params": {}
        }

    # Если ничего не найдено, возвращаем 'chat'
    return {
        "type": "chat",
        "confidence": 0.5,
        "params": {}
    }


def get_optimized_prompt_final(user_now=None, current_time_str=None, current_date_str=None, user_username=None, mentions_str=None, user_memory=None, last_responses=None) -> str:
    """Возвращает оптимизированный системный промпт с динамическими данными"""
    base_prompt = CORE_SYSTEM_PROMPT
    
    # Простая антирепетитивная система - только последние 2 ответа
    if last_responses and len(last_responses) > 0:
        recent = last_responses[:2]
        base_prompt += f"\n\n⚠️ НЕ повторяй: {' | '.join(recent)}"
    
    # Контекст пользователя
    context_parts = []
    if current_time_str:
        context_parts.append(f"Время: {current_time_str}")
    if current_date_str:
        context_parts.append(f"Дата: {current_date_str}")
    if user_username:
        context_parts.append(f"Юзер: {user_username}")
    if mentions_str:
        context_parts.append(mentions_str)
    if user_memory:
        context_parts.append(f"Память: {user_memory}")
    
    if context_parts:
        base_prompt += f"\n\n{' | '.join(context_parts)}"
    
    return base_prompt


def parse_profile_message(message: str) -> dict:
    """Парсит сообщение для извлечения полей профиля"""
    import re
    data = {}
    
    # Приводим к нижнему регистру для поиска
    msg_lower = message.lower()
    
    # Город
    city_match = re.search(r'(?:город|живу в|из)\s*([А-Яа-яA-Za-z\s]+)', message, re.IGNORECASE)
    if city_match:
        data['city'] = city_match.group(1).strip()
    
    # Компания
    company_match = re.search(r'(?:работаю в|компания|фирма)\s*([А-Яа-яA-Za-z\s]+)', message, re.IGNORECASE)
    if company_match:
        data['company'] = company_match.group(1).strip()
    
    # Должность
    position_match = re.search(r'(?:я\s+)?([А-Яа-яA-Za-z\s]+(?:разработчик|менеджер|директор|инженер|аналитик|дизайнер))', message, re.IGNORECASE)
    if position_match:
        data['position'] = position_match.group(1).strip()
    
    # Навыки - если не распарсено в другие поля, добавляем в skills
    if not data:
        data['skills'] = message
    
    return data


def improved_fallback(intent: dict, tool_calls=None, ai_response_content="", message="", user_id=None) -> list:
    """Резервная функция для обработки сообщений - возвращает список действий"""
    # Для простых чатов без действий возвращаем пустой список
    if intent.get("type") == "chat" or intent.get("type") == "conversation":
        return []
    
    # Для complete_task, если tool_calls пустые, вызываем функцию
    if intent.get("type") == "complete_task" and (not tool_calls or len(tool_calls) == 0):
        task_title = intent.get("params", {}).get("task_title")
        if task_title:
            from ai_integration.handlers import complete_task
            result = complete_task(task_title=task_title, user_id=user_id)
            return [{"function": "complete_task", "result": result}]
    
    # Для update_profile, если tool_calls пустые, вызываем функцию
    if intent.get("type") == "update_profile" and (not tool_calls or len(tool_calls) == 0):
        from ai_integration.handlers import update_profile
        # Простой парсинг сообщения для извлечения полей профиля
        parsed_data = parse_profile_message(message)
        result = update_profile(user_id=user_id, **parsed_data)
        return [{"function": "update_profile", "result": result}]

    # Для остальных случаев возвращаем пустой список
    return []

# Новая функция для AI-классификации намерений
async def ai_classify_intent(message: str, mentions_str: str = "", api_key: str = None) -> dict:
    """AI-powered классификация намерений с использованием DeepSeek"""
    import aiohttp
    import json
    
    if not api_key:
        # Fallback to regex-based classification
        return improved_classify_intent(message, mentions_str)
    
    classification_prompt = f"""Ты - эксперт по анализу намерений пользователя в системе управления задачами.

Проанализируй сообщение пользователя и определи:
1. ТИП НАМЕРЕНИЯ (одно из: add_task, complete_task, list_tasks, delegate_task, delete_all_tasks, update_profile, chat)
2. ПАРАМЕТРЫ (если применимо): task_title, reminder_time, has_time
3. УВЕРЕННОСТЬ (от 0.0 до 1.0)

ТИПЫ НАМЕРЕНИЙ:
- add_task: запросы на создание задачи (напомнить, добавить, создать задачу)
- complete_task: отметка задачи как выполненной (сделал, выполнил, готово)
- list_tasks: запрос списка задач (покажи задачи, мои задачи)
- delegate_task: делегирование задачи (@username задача)
- delete_all_tasks: удаление всех задач
- update_profile: обновление профиля (мой город, я разработчик, работаю в компании, моя должность)
- chat: обычный разговор, вопросы, приветствия

ВЕРНИ ТОЛЬКО JSON в формате:
{{
  "type": "тип_намерения",
  "confidence": 0.95,
  "params": {{
    "task_title": "извлеченное название задачи",
    "reminder_time": "извлеченное время",
    "has_time": true
  }}
}}

Сообщение пользователя: {message}
"""

    try:
        url = "https://api.deepseek.com/v1/chat/completions"
        headers = {"Authorization": f"Bearer {api_key}", "Content-Type": "application/json"}
        data = {
            "model": "deepseek-chat",
            "messages": [{"role": "user", "content": classification_prompt}],
            "temperature": 0.1,  # Низкая температура для точности
            "max_tokens": 500
        }
        
        async with aiohttp.ClientSession() as session:
            async with session.post(url, headers=headers, json=data, timeout=aiohttp.ClientTimeout(total=10)) as response:
                if response.status == 200:
                    result = await response.json()
                    content = result["choices"][0]["message"]["content"]
                    
                    # Извлекаем JSON из ответа
                    try:
                        # Ищем JSON в ответе
                        json_match = re.search(r'\{.*\}', content, re.DOTALL)
                        if json_match:
                            intent_data = json.loads(json_match.group(0))
                            return intent_data
                    except json.JSONDecodeError:
                        pass
        
        # Если AI не смог классифицировать, fallback to regex
        return improved_classify_intent(message, mentions_str)
        
    except Exception as e:
        import logging
        logger = logging.getLogger(__name__)
        logger.warning(f"Error in AI classification: {e}")
        # Fallback to regex
        return improved_classify_intent(message, mentions_str)

