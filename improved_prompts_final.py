def improved_classify_intent(message: str, mentions_str: str = "") -> dict:
    """Улучшенная классификация намерений пользователя с поддержкой русского языка"""
    message_lower = message.lower().strip()

    # Проверяем наличие временных указаний для задач
    time_indicators = [
        'через', 'в', 'на', 'завтра', 'послезавтра', 'сегодня', 'вечером', 'утром', 'днем',
        'ночью', 'минут', 'час', 'день', 'недел', 'месяц', 'год', 'январ', 'феврал', 'март',
        'апрел', 'май', 'июн', 'июл', 'август', 'сентябр', 'октябр', 'ноябр', 'декабр',
        'понедельник', 'вторник', 'среда', 'четверг', 'пятница', 'суббота', 'воскресенье'
    ]
    has_time = any(indicator in message_lower for indicator in time_indicators)

    # Шаблоны для различных команд (проверяем ДО приветствий!)
    patterns = {
        'add_task': [
            r'^напомни(?:ть)?\s+(.+)',
            r'^добавь\s+(.+)',
            r'^запомни\s+(.+)',
            r'^создай\s+задачу\s+(.+)',
            r'^новая\s+задача\s+(.+)',
            r'^задача\s+(.+)',
            r'^напомни\s+о\s+(.+)',
            r'^напомни\s+мне\s+(.+)',
            r'напомни(?:ть)?\s+мне',
            r'добавь\s+задачу',
            r'запомни\s+задачу'
        ],
        'list_tasks': [
            r'покажи\s+задачи',
            r'список\s+задач',
            r'мои\s+задачи',
            r'что\s+на\s+сегодня',
            r'что\s+запланировано',
            r'какие\s+задачи',
            r'^список$',
            r'^задачи$'
        ],
        'complete_task': [
            r'сделал\s+(.+)',
            r'выполнил\s+(.+)',
            r'завершил\s+(.+)',
            r'готово\s+(.+)',
            r'^готово$',
            r'сделано\s+(.+)',
            r'завершить\s+(.+)',
            r'я\s+созвонился',
            r'я\s+выполнил'
        ],
        'delegate_task': [
            r'@(\w+)\s+(.+)',
            r'поручи\s+@(\w+)\s+(.+)',
            r'делегируй\s+@(\w+)\s+(.+)',
            r'передай\s+@(\w+)\s+(.+)'
        ],
        'delete_all_tasks': [
            r'удали\s+все',
            r'очисти\s+все',
            r'удалить\s+все\s+задачи',
            r'очистить\s+список'
        ]
    }

    # Проверяем каждый паттерн
    for intent_type, pattern_list in patterns.items():
        for pattern in pattern_list:
            match = re.search(pattern, message_lower)
            if match:
                return {
                    "type": intent_type,
                    "confidence": 0.9,
                    "params": {"has_time": has_time}
                }

    # Простые приветствия - проверяем ПОСЛЕ задач
    greetings = [
        r'^привет$', r'^здравствуй$', r'^добрый день$', r'^доброе утро$', r'^добрый вечер$',
        r'^хай$', r'^hello$', r'^hi$', r'^hey$', r'^ку$', r'^здорово$',
        r'^привет!$', r'^здравствуй!$'
    ]
    if any(re.search(greeting, message_lower) for greeting in greetings):
        return {
            "type": "chat",
            "confidence": 0.95,
            "params": {}
        }

    # Если ничего не найдено, возвращаем 'chat'
    return {
        "type": "chat",
        "confidence": 0.5,
        "params": {}
    }


def get_optimized_prompt_final(user_now=None, current_time_str=None, current_date_str=None, user_username=None, mentions_str=None, user_memory=None, last_responses=None) -> str:
    """Возвращает оптимизированный системный промпт с динамическими данными"""
    base_prompt = CORE_SYSTEM_PROMPT
    
    # Простая антирепетитивная система - только последние 2 ответа
    if last_responses and len(last_responses) > 0:
        recent = last_responses[:2]
        base_prompt += f"\n\n⚠️ НЕ повторяй: {' | '.join(recent)}"
    
    # Контекст пользователя
    context_parts = []
    if current_time_str:
        context_parts.append(f"Время: {current_time_str}")
    if current_date_str:
        context_parts.append(f"Дата: {current_date_str}")
    if user_username:
        context_parts.append(f"Юзер: {user_username}")
    if mentions_str:
        context_parts.append(mentions_str)
    if user_memory:
        context_parts.append(f"Память: {user_memory}")
    
    if context_parts:
        base_prompt += f"\n\n{' | '.join(context_parts)}"
    
    return base_prompt


