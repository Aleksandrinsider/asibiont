def improved_classify_intent(message, mentions_str):
    """
    Улучшенная классификация с четкими приоритетами
    """
    message_lower = message.lower().strip()
    intent = {"type": "unknown", "confidence": 0.0, "params": {}}
    
    # Короткие ответы (1-3 слова) без команд = диалог
    words = message_lower.split()
    command_keywords = ["покажи", "список", "добавь", "удали", "напомни", "создай", 
                       "поручи", "выполнил", "сделал", "найди", "@"]
    if len(words) <= 3 and not any(keyword in message_lower for keyword in command_keywords):
        intent["type"] = "conversation"
        intent["confidence"] = 0.9
        return intent
    
    # TIER 1: КРИТИЧНЫЕ КОМАНДЫ (0.95+)
    if "@" in message:
        intent["type"] = "delegate_task"
        intent["confidence"] = 0.95
        intent["params"]["delegated_to"] = mentions_str
        return intent
    
    if any(word in message_lower for word in ["выполнил", "сделал", "завершил", "готово"]):
        intent["type"] = "complete_task"
        intent["confidence"] = 0.95
        for keyword in ["выполнил", "сделал", "завершил"]:
            if keyword in message_lower:
                parts = message.split(keyword, 1)
                if len(parts) > 1:
                    intent["params"]["task_title"] = parts[1].strip()
                break
        return intent
    
    if "удали все" in message_lower or "очисти все" in message_lower:
        intent["type"] = "delete_all_tasks"
        intent["confidence"] = 0.95
        return intent
    
    # TIER 2: ЧАСТЫЕ КОМАНДЫ (0.8-0.9)
    if any(word in message_lower for word in ["покажи", "список", "мои задачи", "что делать"]):
        intent["type"] = "list_tasks"
        intent["confidence"] = 0.85
        return intent
    
    if any(word in message_lower for word in ["напомни", "добавь", "создай задачу", "запланируй"]):
        intent["type"] = "add_task"
        intent["confidence"] = 0.85
        time_match = re.search(r"(\d{4}-\d{2}-\d{2} \d{1,2}:\d{2})", message)
        if time_match:
            intent["params"]["reminder_time"] = time_match.group(1)
        return intent
    
    # TIER 3: КОНТЕКСТНЫЕ КОМАНДЫ (0.7-0.8)
    if any(word in message_lower for word in ["найди", "партнёр", "людей", "кто может"]):
        intent["type"] = "find_partners"
        intent["confidence"] = 0.75
        return intent
    
    if any(word in message_lower for word in ["живу в", "работаю", "интересуюсь", "мои навыки"]):
        intent["type"] = "update_profile"
        intent["confidence"] = 0.7
        city_match = re.search(r"живу в\s+(.+?)(?:\s|$|,)", message_lower)
        if city_match:
            intent["params"]["city"] = city_match.group(1).strip().title()
        return intent
    
    # TIER 4: ВОПРОСЫ/ДИАЛОГ (0.5-0.6)
    if any(word in message_lower for word in ["как", "что", "почему", "помоги", "совет"]):
        intent["type"] = "conversation"
        intent["confidence"] = 0.6
        return intent
    
    # DEFAULT: НЕЯСНО
    intent["type"] = "unknown"
    intent["confidence"] = 0.3
    return intent


def improved_fallback(intent, tool_calls, ai_response, message, user_id):
    """
    Умный fallback - срабатывает ТОЛЬКО когда AI точно ошибся
    """
    # ПРАВИЛО 1: Если AI вызвал tool calls - НЕ трогаем
    if tool_calls:
        return []
    
    # ПРАВИЛО 2: Если intent уверенность < 0.8 - НЕ применяем fallback
    if intent["confidence"] < 0.8:
        return []
    
    # ПРАВИЛО 3: Если AI дал содержательный ответ (> 50 символов) - НЕ трогаем
    if len(ai_response.strip()) > 50:
        return []
    
    # ПРАВИЛО 4: Применяем fallback ТОЛЬКО для критичных команд
    critical_intents = ["delegate_task", "complete_task", "delete_all_tasks", "add_task", "list_tasks"]
    if intent["type"] not in critical_intents:
        return []
    
    # Теперь точно нужен fallback
    import logging
    logging.warning(f"[FALLBACK] Applying for intent: {intent['type']}, confidence: {intent['confidence']}")
    
    fallback_actions = []
    
    if intent["type"] == "delegate_task":
        fallback_actions.append({
            "function": "delegate_task",
            "params": intent["params"],
            "reason": "AI не распознал делегирование с @mention"
        })
    elif intent["type"] == "complete_task":
        fallback_actions.append({
            "function": "complete_task",
            "params": intent["params"],
            "reason": "AI не завершил задачу"
        })
    elif intent["type"] == "add_task":
        fallback_actions.append({
            "function": "add_task",
            "params": intent["params"],
            "reason": "AI не добавил задачу"
        })
    elif intent["type"] == "list_tasks":
        fallback_actions.append({
            "function": "list_tasks",
            "params": {},
            "reason": "AI не показал список"
        })
    elif intent["type"] == "delete_all_tasks":
        fallback_actions.append({
            "function": "delete_all_tasks",
            "params": {},
            "reason": "AI не удалил все задачи"
        })
    
    return fallback_actions


