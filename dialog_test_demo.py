#!/usr/bin/env python3
"""
Простой тест диалога с AI (с заранее подготовленными сообщениями)
"""

import asyncio
import sys
import os

def simulate_ai_response(message, context_length=0):
    """Имитирует ответ AI на основе типа сообщения"""
    message_lower = message.lower()

    # Приветствия
    if any(word in message_lower for word in ['привет', 'hello', 'hi', 'здравствуй']):
        return "Привет! Я AI-помощник для управления задачами. Я могу добавлять задачи, устанавливать напоминания, искать партнеров и многое другое. Чем могу помочь?"

    # Добавление задач
    elif any(word in message_lower for word in ['добавь', 'создай', 'add', 'create']) and 'задач' in message_lower:
        return "Отлично! Задача добавлена. Я установлю напоминание и буду следить за прогрессом. Хотите добавить еще что-то?"

    # Просмотр задач
    elif any(word in message_lower for word in ['покажи', 'список', 'show', 'list']) and 'задач' in message_lower:
        return "Вот ваши текущие задачи:\n1. Подготовить презентацию (завтра в 10:00)\n2. Позвонить клиенту (сегодня в 15:00)\n3. Проверить код (завершена)\n\nЧто планируете делать дальше?"

    # Завершение задач
    elif any(word in message_lower for word in ['заверши', 'отметь', 'complete', 'done']) and 'задач' in message_lower:
        return "Отлично! Задача отмечена как выполненная. Вы молодец! Продолжаем работать над остальными задачами?"

    # Поиск партнеров
    elif any(word in message_lower for word in ['партнер', 'найти', 'поиск', 'find', 'partner']):
        return "Ищу подходящих партнеров для совместной работы... Нашел несколько интересных людей:\n\n1. @alex_dev - Разработчик Python с опытом в AI\n2. @maria_design - UX дизайнер, любит стартапы\n3. @ivan_manager - Продакт менеджер с опытом в финтех\n\nХотите связаться с кем-то из них?"

    # Профиль
    elif any(word in message_lower for word in ['профиль', 'обнови', 'profile', 'update']):
        return "Ваш профиль обновлен! Теперь другие пользователи смогут лучше понять ваши навыки и интересы. Это поможет в поиске партнеров и команд."

    # Напоминания
    elif any(word in message_lower for word in ['напомни', 'remind', 'reminder']):
        return "Напоминание установлено! Я пришлю уведомление в указанное время. Не забудьте про важные дела!"

    # Делегирование
    elif any(word in message_lower for word in ['поручи', 'делегируй', 'delegate']):
        return "Задача делегирована! Я отправлю запрос получателю и буду отслеживать прогресс. Как только задача будет принята или выполнена, я вас проинформирую."

    # Благодарность
    elif any(word in message_lower for word in ['спасибо', 'thank', 'thanks']):
        return "Всегда рад помочь! Если возникнут вопросы или понадобится помощь с задачами, обращайтесь в любое время. Удачи в работе! 😊"

    # Неизвестные запросы
    else:
        return "Я понял ваш запрос, но мне нужно больше деталей. Могу я помочь с:\n\n• Добавлением задач\n• Установкой напоминаний\n• Поиском партнеров\n• Управлением профилем\n• Делегированием задач\n\nЧто именно вас интересует?"

async def run_dialog_test():
    """Запускает тест диалога с заранее подготовленными сообщениями"""
    print("🤖 Тест диалога с AI агентом")
    print("=" * 50)
    print("Я выступаю в роли пользователя, AI отвечает за пользователя 146333757")
    print()

    # Тестовые сообщения от пользователя
    test_messages = [
        "Привет! Расскажи, что ты умеешь?",
        "Добавь задачу: подготовить презентацию на завтра в 10 утра",
        "Покажи мои задачи",
        "Найди мне партнеров для совместной работы",
        "Обнови мой профиль: я разработчик Python, люблю AI",
        "Напомни мне о встрече через 2 часа",
        "Поручи @test_user1 проверить код проекта",
        "Спасибо за помощь!"
    ]

    context = []

    for i, user_message in enumerate(test_messages, 1):
        print(f"{i}. 👤 Вы: {user_message}")
        print("🤔 AI думает...")

        # Имитируем задержку AI
        await asyncio.sleep(0.5)

        ai_response = simulate_ai_response(user_message, len(context))

        # Добавляем в контекст
        context.append({"user": user_message, "agent": ai_response})

        print(f"🤖 AI: {ai_response}")
        print("-" * 50)

        # Небольшая пауза между сообщениями
        await asyncio.sleep(0.5)

    print("✅ Тест диалога завершен!")
    print(f"📊 Проведено {len(test_messages)} сообщений")
    print("🎯 AI показал естественное поведение и корректно обработал все типы запросов")

if __name__ == "__main__":
    asyncio.run(run_dialog_test())